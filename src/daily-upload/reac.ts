久远也。
久者，不暂也。远者，不近也。引伸之为滋长，长幼之长。今音知丈切。又为多馀之长，度长之长皆今音直亮切。兄下曰：长也。是滋长，长幼之长也。
从兀，从匕。
会意。匕呼霸切。
亾声。
二字各本在变匕之下，今依韵会正。直良切。十部。
兀者、高远意也。
说从兀之意。儿部曰：兀者、高而上平也。
久则变匕。
匕各本作化。今正。说从匕之意。匕下曰：变也。
〔⿱厂丫〕者，到亾也。
到各本作倒。今正。说？卽亾字。亾而倒变匕之意。凡长之属皆从长。 [7]
